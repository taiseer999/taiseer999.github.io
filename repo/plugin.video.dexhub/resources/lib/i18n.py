import xbmcaddon

ADDON = xbmcaddon.Addon()
_lang_cache = None


def current_language():
    global _lang_cache
    if _lang_cache is not None:
        return _lang_cache
    try:
        raw = (ADDON.getSetting('ui_language') or 'English').strip().lower()
    except Exception:
        raw = 'english'
    compact = raw.replace('_', ' ').replace('-', ' ').strip()
    if compact in ('en', 'english') or compact.startswith('en'):
        _lang_cache = 'en'
    else:
        _lang_cache = 'ar'
    return _lang_cache


def is_english():
    return current_language() == 'en'


