_SORTED_KEYS = sorted(STRINGS.keys(), key=len, reverse=True)


def tr(text):
    if text is None:
        return text
    if not isinstance(text, str) or not is_english():
        return text
    exact = STRINGS.get(text)
    if exact is not None:
        return exact
    out = text
    for key in _SORTED_KEYS:
        if key in out:
            out = out.replace(key, STRINGS[key])
    return out


def trf(template, *args, **kwargs):
    translated = tr(template)
    try:
        if args or kwargs:
            return translated % (args if len(args) != 1 else args[0]) if args else translated.format(**kwargs)
        return translated
    except Exception:
        return translated
